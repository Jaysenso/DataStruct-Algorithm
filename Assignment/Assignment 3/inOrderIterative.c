void inOrderTraversal(BSTNode *root)
{
    if (root == NULL) 
        return;

    BSTNode *curr = root;
    BSTNode *popped;
    Stack s;
    s.top = NULL;

    while(1)
    {
        //traverse down left-sub tree till we hit rock bottom
        while(curr != NULL)
        {   
            push(&s,curr);
            curr = curr->left;
        }

        /*once curr == NULL -> the top item in the stack will be the left-mode node
        1. we will pop and print the node
        2.we will update curr to the popped->right node
        3. if its == NULL && stack isnt empty, we will pop the top item again and update curr
            -> we will see that our curr will bring us back up to the root node since its getting updated by the stack's item.
        4. this cycle repeats till we print all the nodes in the BST
        */
        
        if(!isEmpty(&s))
        {
            popped = pop(&s);
            printf("%d ", popped->item);
            curr = popped->right;
        }
        //this will break us out of the while loop -> when curr= NULL and stack is empty
        if(curr == NULL && isEmpty(&s))
            break;
    }

}

